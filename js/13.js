// Function Declaration Funcion de declaracion o declarativa

//definiento funcion

function sumar() {
  console.log(1 + 1);
}

//llamando funcion

sumar();

// funcion con parametros para hacer logica con informacion del exterior

function sumarParametros(n1, n2) {
  const nt = n1 + n2;
  console.log(nt);
}

sumarParametros(10, 20);
sumarParametros(50, 50);
